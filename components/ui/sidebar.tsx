const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContextProps = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
  isTransitioning: boolean
  isInitialLoad: boolean
}

const SidebarContext = React.createContext<SidebarContextProps | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

function SidebarProvider({
  defaultOpen = true,
  open: openProp,
  onOpenChange: setOpenProp,
  className,
  style,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  defaultOpen?: boolean
  open?: boolean
  onOpenChange?: (open: boolean) => void
}) {
  const isMobile = useIsMobile()
  const [openMobile, setOpenMobile] = React.useState(false)
  const [isTransitioning, setIsTransitioning] = React.useState(false)
  const [isInitialLoad, setIsInitialLoad] = React.useState(true)

  // Disable animations on initial mount/refresh
  React.useEffect(() => {
    // Disable animations for initial render (including refresh)
    // Use a longer delay to ensure all initial renders complete
    const timer = setTimeout(() => {
      setIsInitialLoad(false)
    }, 300)
    return () => clearTimeout(timer)
  }, [])

  // This is the internal state of the sidebar.
  // We use openProp and setOpenProp for control from outside the component.
  const [_open, _setOpen] = React.useState(defaultOpen)
  const open = openProp ?? _open
  const setOpen = React.useCallback(
    (value: boolean | ((value: boolean) => boolean)) => {
      const openState = typeof value === "function" ? value(open) : value
      if (setOpenProp) {
        setOpenProp(openState)
      } else {
        _setOpen(openState)
      }

      // This sets the cookie to keep the sidebar state using cookie utility
      UIStateCookies.setSidebarOpen(openState);
    },
    [setOpenProp, open]
  )

  // Helper to toggle the sidebar.
  const toggleSidebar = React.useCallback(() => {
    if (!isMobile) {
      const willCollapse = open // If currently open, will collapse
      if (willCollapse) {
        // Only hide text when collapsing (expanded -> collapsed)
        setIsTransitioning(true)
      }
      setOpen((open) => !open)
      // Hide text during transition (500ms duration) only when collapsing
      if (willCollapse) {
        setTimeout(() => {
          setIsTransitioning(false)
        }, 500)
      }
    } else {
      setOpenMobile((open) => !open)
    }
  }, [isMobile, setOpen, setOpenMobile, open])

  // Adds a keyboard shortcut to toggle the sidebar.
  React.useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (
        event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
        (event.metaKey || event.ctrlKey)
      ) {
        event.preventDefault()
        toggleSidebar()
      }
    }

    window.addEventListener("keydown", handleKeyDown)
    return () => window.removeEventListener("keydown", handleKeyDown)
  }, [toggleSidebar])

  // We add a state so that we can do data-state="expanded" or "collapsed".
  // This makes it easier to style the sidebar with Tailwind classes.
  const state = open ? "expanded" : "collapsed"

  const contextValue = React.useMemo<SidebarContextProps>(
    () => ({
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
      isTransitioning,
      isInitialLoad,
    }),
    [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar, isTransitioning, isInitialLoad]
  )

  return (
    <SidebarContext.Provider value={contextValue}>
      <TooltipProvider delayDuration={0}>
        <div
          data-slot="sidebar-wrapper"
          style={
            {
              "--sidebar-width": SIDEBAR_WIDTH,
              "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
              ...style,
            } as React.CSSProperties
          }
          className={cn(
            "group/sidebar-wrapper has-data-[variant=inset]:bg-sidebar flex min-h-svh w-full",
            className
          )}
          {...props}
        >
          {children}
        </div>
      </TooltipProvider>
    </SidebarContext.Provider>
  )
}

function Sidebar({
  side = "left",
  variant = "sidebar",
  collapsible = "icon",
  className,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  side?: "left" | "right"
  variant?: "sidebar" | "floating" | "inset"
  collapsible?: "offcanvas" | "icon" | "none"
}) {
  const { isMobile, state, openMobile, setOpenMobile, isInitialLoad } = useSidebar()

  if (collapsible === "none") {
    return (
      <div
        data-slot="sidebar"
        className={cn(
          "bg-sidebar text-sidebar-foreground flex h-full w-(--sidebar-width) flex-col",
          className
        )}
        {...props}
      >
        {children}
      </div>
    )
  }

  if (isMobile) {
    return (
      <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
        <SheetContent
          data-sidebar="sidebar"
          data-slot="sidebar"
          data-mobile="true"
          className="bg-sidebar text-sidebar-foreground w-(--sidebar-width) p-0 [&>button]:hidden"
          style={
            {
              "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
            } as React.CSSProperties
          }
          side={side}
        >
          <SheetHeader className="sr-only">
            <SheetTitle>Sidebar</SheetTitle>
            <SheetDescription>Displays the mobile sidebar.</SheetDescription>
          </SheetHeader>
          <div className="flex h-full w-full flex-col">{children}</div>
        </SheetContent>
      </Sheet>
    )
  }

  const isCollapsed = state === "collapsed"
  
  // Calculate widths based on variant and state
  const getGapWidth = () => {
    if (collapsible === "offcanvas") return "0"
    if (isCollapsed) {
      return variant === "floating" || variant === "inset" 
        ? `calc(${SIDEBAR_WIDTH_ICON} + 1rem)`
        : SIDEBAR_WIDTH_ICON
    }
    return SIDEBAR_WIDTH
  }
  
  const getContainerWidth = () => {
    if (collapsible === "offcanvas") return SIDEBAR_WIDTH
    if (isCollapsed) {
      return variant === "floating" || variant === "inset"
        ? `calc(${SIDEBAR_WIDTH_ICON} + 1rem + 2px)`
        : SIDEBAR_WIDTH_ICON
    }
    return SIDEBAR_WIDTH
  }
  
  const getContainerPosition = () => {
    if (collapsible === "offcanvas" && isCollapsed) {
      return side === "left" 
        ? `calc(${SIDEBAR_WIDTH} * -1)`
        : `calc(${SIDEBAR_WIDTH} * -1)`
    }
    return "0"
  }

  return (
    <div
      className="group peer text-sidebar-foreground hidden md:block h-screen"
      data-state={state}
      data-collapsible={state === "collapsed" ? collapsible : ""}
      data-variant={variant}
      data-side={side}
      data-slot="sidebar"
      data-initial-load={isInitialLoad}
    >
      {/* This is what handles the sidebar gap on desktop */}
      <motion.div
        data-slot="sidebar-gap"
        className={cn(
          "relative bg-transparent",
          "group-data-[side=right]:rotate-180"
        )}
        initial={isInitialLoad ? { width: getGapWidth() } : false}
        animate={{
          width: getGapWidth(),
        }}
        transition={{
          duration: isInitialLoad ? 0 : (state === "expanded" ? 0.35 : 0.5),
          ease: [0.25, 0.1, 0.25, 1],
        }}
      />
      <motion.div
        data-slot="sidebar-container"
        className={cn(
          "fixed inset-y-0 z-10 hidden h-svh md:flex",
          variant === "floating" || variant === "inset"
            ? "p-2"
            : "group-data-[side=left]:border-r group-data-[side=right]:border-l",
          className
        )}
        initial={isInitialLoad ? { 
          width: getContainerWidth(),
          [side === "left" ? "left" : "right"]: getContainerPosition()
        } : false}
        animate={{
          width: getContainerWidth(),
          [side === "left" ? "left" : "right"]: getContainerPosition(),
        }}
        transition={{
          duration: isInitialLoad ? 0 : (state === "expanded" ? 0.35 : 0.5),
          ease: [0.25, 0.1, 0.25, 1],
        }}
        {...(props as Omit<typeof props, "onAnimationStart" | "onAnimationEnd" | "onAnimationIteration" | "onDragStart" | "onDrag" | "onDragEnd">)}
      >
        <div
          data-sidebar="sidebar"
          data-slot="sidebar-inner"
          className="bg-sidebar group-data-[variant=floating]:border-sidebar-border flex h-full w-full flex-col overflow-x-hidden group-data-[variant=floating]:rounded-lg group-data-[variant=floating]:border group-data-[variant=floating]:shadow-sm"
        >
          {children}
        </div>
      </motion.div>
    </div>
  )
}

function SidebarTrigger({
  className,
  onClick,
  ...props
}: React.ComponentProps<typeof Button>) {
  const { toggleSidebar, state } = useSidebar()
  const { isRTL } = useLocale()
  const isCollapsed = state === "collapsed"
  
  // Reverse icons only for English (LTR), keep normal for Arabic (RTL)
  const shouldReverseIcons = !isRTL

  return (
    <Button
      data-sidebar="trigger"
      data-slot="sidebar-trigger"
      variant="ghost"
      size="icon"
      className={cn(ICON_BUTTON_CLASSES, className)}
      onClick={(event) => {
        onClick?.(event)
        toggleSidebar()
      }}
      {...props}
    >
      <AnimateIcon animateOnHover>
        {shouldReverseIcons ? (
          // English (LTR): reversed icons
          isCollapsed ? (
            <PanelLeftOpen size={DEFAULT_ICON_SIZE} />
          ) : (
            <PanelLeftClose size={DEFAULT_ICON_SIZE} />
          )
        ) : (
          // Arabic (RTL): normal icons
          isCollapsed ? (
            <PanelLeftClose size={DEFAULT_ICON_SIZE} />
          ) : (
            <PanelLeftOpen size={DEFAULT_ICON_SIZE} />
          )
        )}
      </AnimateIcon>
      <span className="sr-only">Toggle Sidebar</span>
    </Button>
  )
}

